package by.ivankov.task3.entity;


public class CustomArray {
    private int arrayId;
    private int[] array;

    public CustomArray(){
    }

    public int getArrayId() {
        return arrayId;
    }

    public void setArrayId(int arrayId) {
        this.arrayId = arrayId;
    }

    public void setArray(int[] array) {
        this.array = array;
    }

    public int[] getArray() {
        return array;
    }
}
